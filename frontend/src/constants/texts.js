export function buildSearchSummary({ loading, total, count }) {
  if (loading) {
    return '\u6b63\u5728\u641c\u7d22\u4e2d'
  }

  if (!count) {
    return '\u8f93\u5165\u5173\u952e\u8bcd\u540e\u5f00\u59cb\u641c\u7d22'
  }

  return `\u5171\u627e\u5230 ${total || count} \u9996`
}

export function buildSearchPageLabel({ loading, page, totalPages, count }) {
  if (loading || !count) {
    return ''
  }

  return `\u7b2c ${page} / ${totalPages} \u9875`
}

export function buildSearchLoadedLabel({ count, total }) {
  if (!count) {
    return ''
  }

  return `\u5df2\u52a0\u8f7d ${count} / ${total || count}`
}
